def printb(mes):
    # blue
    print("\033[0;34m" + str(mes) + "\033[0;39m")

def printy(mes):
    # yellow
    print("\033[0;33m" + str(mes) + "\033[0;39m")

def printg(mes):
    # green
    print("\033[0;32m" + str(mes) + "\033[0;39m")

def printr(mes):
    # red
    print("\033[0;31m" + str(mes) + "\033[0;39m")
